#[test]
fn convert_definitions() {
    for def in &FUNCTION_DEFINITIONS {
        print!(
            "FunctionDefinition {{ name: \"{}\", opcode: FuncOpCode::{:?}, args: ",
            def.name, def.opcode
        );
        if def.args < 0 {
            print!("{}", def.args);
        } else {
            print!("0x{:02X}", def.args);
        }
        if def.args < 0 || def.args == 0x10 || def.args == 0x11 {
            print!(", function: predefined_functions::invalid");
        } else {
            print!(
                ", function: predefined_functions::{}",
                def.name.to_lowercase()
            );
        }
        println!(" }},");
    }
}
