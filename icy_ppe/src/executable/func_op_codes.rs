pub const LAST_FUNC: i16 = -290;
impl FuncOpCode {
    pub fn get_definition(self) -> &'static FunctionDefinition {
        let i = -(self as i16);
        &FUNCTION_DEFINITIONS[i as usize]
    }
}

type PredefFunc = fn(vm: &mut crate::vm::VirtualMachine, params: &[Variable]) -> Res<Variable>;

#[derive(Debug, PartialEq)]
pub struct FunctionDefinition {
    pub name: &'static str,
    pub opcode: FuncOpCode,
    pub args: i8,
    pub function: PredefFunc,
}

pub fn get_function_definition(str: &str) -> i32 {
    for (i, item) in FUNCTION_DEFINITIONS.iter().enumerate() {
        if item.name.eq_ignore_ascii_case(str) {
            return i as i32;
        }
    }
    -1
}

