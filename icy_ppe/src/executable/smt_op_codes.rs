impl OpCode {
    pub fn get_definition(self) -> &'static StatementDefinition {
        &STATEMENT_DEFINITIONS[self as usize]
    }
}

impl Display for OpCode {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        write!(f, "{self:?}")
    }
}

